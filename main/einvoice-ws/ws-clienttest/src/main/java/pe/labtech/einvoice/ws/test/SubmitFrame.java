    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        //hierarchy construction
        final PortInfo portInfo = new PortInfo();
        portInfo.init();
        final PortProducer pp = new PortProducer(
                new EBizGenericInvokerImplService(),
                portInfo
        );
        pp.init();

        EBizGenericInvoker port = pp.getPort();

        port.invokeAsync(source.getText(), t -> {
            try {
                this.submit.setEnabled(false);
                this.target.setText("");
                InvokeResponse resp = t.get();
                target.setText(resp.getReturn());
            } catch (InterruptedException ex) {
                Logger.getLogger(SubmitFrame.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ExecutionException ex) {
                //falla del proceso
                StringWriter sw = new StringWriter();
                try (PrintWriter pw = new PrintWriter(sw)) {
                    ex.getCause().printStackTrace(pw);
                }
                target.setText(sw.toString());
            } finally {
                this.submit.setEnabled(true);
            }
        });

    }//GEN-LAST:event_submitActionPerformed

    private void templatesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_templatesActionPerformed
        try (Scanner scanner = new Scanner(new File((String) this.templates.getSelectedItem()), "UTF-8")) {
            scanner.useDelimiter("\\Z");
            this.source.setText(scanner.next());
        } catch (IOException ex) {
            Logger.getLogger(SubmitFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_templatesActionPerformed

