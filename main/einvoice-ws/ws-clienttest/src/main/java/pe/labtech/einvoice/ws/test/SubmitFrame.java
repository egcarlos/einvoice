    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        //establecer la posición del componente
        proportion = 0.5d;
        jSplitPane1.setDividerLocation(proportion);

        //mantener la relación de aspecto despues de cambiado el tamaño
        Arrays.stream(jSplitPane1.getComponents())
                .filter(c -> c instanceof BasicSplitPaneDivider)
                .forEach(c -> c.addMouseListener(new MouseAdapter() {

                    @Override
                    public void mouseReleased(MouseEvent e) {
                        proportion = 1d * jSplitPane1.getDividerLocation() / jSplitPane1.getWidth();
//                        System.out.println(proportion);
                    }

                }));

        //escanea y agrega los valores del combo box
        Arrays.stream(new File(".").list())
                .filter(n -> n.startsWith("template"))
                .forEach(n -> ((DefaultComboBoxModel<String>) this.templates.getModel()).addElement(n));

    }//GEN-LAST:event_formWindowOpened

